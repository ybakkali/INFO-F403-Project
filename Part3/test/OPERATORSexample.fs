BEGINPROG OPERATORSexample

/*
    A program example showing that all operators work well
*/

number := 10
true := 1
false := 0

// "=" operator
IF (number = 10) THEN
    PRINT(true)
ELSE
    PRINT(false)
ENDIF

// "!=" operator
IF (number != 100) THEN
    PRINT(true)
ELSE
    PRINT(false)
ENDIF

// ">" operator
IF (number > 10) THEN
    PRINT(true)
ELSE
    PRINT(false)
ENDIF

// ">=" operator
IF (number >= 10) THEN
    PRINT(true)
ELSE
    PRINT(false)
ENDIF


// "<" operator
IF (number < 10) THEN
    PRINT(true)
ELSE
    PRINT(false)
ENDIF


// "<=" operator
IF (number <= 10) THEN
    PRINT(true)
ELSE
    PRINT(false)
ENDIF

ENDPROG
